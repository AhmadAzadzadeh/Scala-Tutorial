val from1To5 = 1 to 5
println(from1To5)

val from1Until5 = 1 until 5
println(from1Until5)

val from0To1By2 = 0 to 10 by 2

println(from0To1By2)

val alphabetRangeFromAToZ = 'a' to 'z'
println(alphabetRangeFromAToZ)

val listFrom1To5 = (1 to 5).toList
println(listFrom1To5)

val setFrom1To5 = (1 to 5).toSet
println(setFrom1To5)

val sequenceFrom1To5 = (1 to 5).toSeq
println(sequenceFrom1To5.mkString(" "))

val arrayFrom1To5 = (1 to 5).toArray
println(arrayFrom1To5.mkString(" "))